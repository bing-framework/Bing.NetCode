            mimeDic.Add("xpi", "application/x-xpinstall");
            mimeDic.Add("xpm", "image/x-xpixmap");
            mimeDic.Add("xsit", "text/xml");
            mimeDic.Add("xsl", "text/xml");
            mimeDic.Add("xul", "text/xul");
            mimeDic.Add("xwd", "image/x-xwindowdump");
            mimeDic.Add("xyz", "chemical/x-pdb");
            mimeDic.Add("yz1", "application/x-yz1");
            mimeDic.Add("z", "application/x-compress");
            mimeDic.Add("zac", "application/x-zaurus-zac");
            mimeDic.Add("zip", "application/zip");
        }

        /// <summary>
        /// mimetype
        /// </summary>
        /// <param name="fileType">Type of the file.</param>
        /// <returns>System.String.</returns>
        public static string GetMime(string fileType)
        {
            fileType = fileType.ToLower().Replace(".", "");
            if (mimeDic.ContainsKey(fileType))
            {
                return mimeDic[fileType];
            }
            else
            {
                return "application/octet-stream";
            }
        }
